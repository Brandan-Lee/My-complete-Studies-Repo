# Please type in a year: 1572

#Mary is valiant knight, born in the year 1572. One morning Mary woke up to an awful racket: a dragon was
#  approaching the village. Only Mary could save the village's residents.

#The story should change according to the input given by the user.

# Write your solution here
#recieve input
name = input("Please type in a name: ")
year = input("Please type in a year: ")
#display output
print(name + " is valiant knight, born in the year " + year + ". One morning " + name + " woke up to an awful racket: a dragon was approaching the village. Only " + name + " could save the village's residents.")

