#Please write a program which asks the user for two numbers. The program will then print out the sum and the
#  product of the two numbers.

# Write your solution here
num1 = int(input("Number 1: "))
num2 = int(input("Number 2: "))
#add numbers together to get total sum
sum = num1 + num2
#multiply numbers together to get total product
product = num1 * num2

print(f"The sum of the numbers: {sum}")
print(f"The product of the numbers: {product}")

#examples of output
#Number 1: 3
# Number 2: 7
# The sum of the numbers: 10
# The product of the numbers: 21