#Please write a program which asks the user to type in a string. The program then prints each input
#  character on a separate line. After each character there should be a star (*) printed on its own
#  line.

# Write your solution here
text = input("Please type in a string: ")
#loop for character in item and print star after each character
for character in text:
    print(character)
    print("*")

